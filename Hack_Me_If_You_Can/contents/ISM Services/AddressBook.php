<p>The address book contains all the address and telephone details of our employees. You can search for a particular employee within this portal. You can also add your own details in this portal. Use this portal to add your contact details if you are a new employee or just update them in case they have been changed.</p>
<p>Remember you only have the permission to update your own contact details. Updating or deleting records of another employee is not allowed and if you are found to be doing so, will result in disciplinary actions.</p>
<p>
You can add the following items to the address book.
<ul class="list-group">
  <li class="list-group-item">First name (Mandatory)</li>
  <li class="list-group-item">Last name (Mandatory)</li>
  <li class="list-group-item">Address (Mandatory)</li>
  <li class="list-group-item">Telephone number (Optional)</li>
  <li class="list-group-item">E-mail address (Mandatory)</li>
  <li class="list-group-item">Mobile phone number (Mandatory)</li>
</ul>
</p>
